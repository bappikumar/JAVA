public class Swaping {
    public static void main(String[] args) {
        int num1 = 34;
        int num2 = 55;
        System.out.println("Number1: " + num1 + " Number2: "+ num2);
        int temp = num1;
        System.out.println("Temp: " + num1);
        num1 = num2;
        System.out.println("Num1: " + num1);
        num2 = temp;
        System.out.println("Num2: " + num2);

    }
    
}
